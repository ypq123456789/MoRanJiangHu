
// 物品系统核心定义 (全中文变量)

export type 物品类型 = '武器' | '防具' | '饰品' | '消耗品' | '材料' | '秘籍' | '杂物' | '杂项';
export type 物品品质 = '凡品' | '良品' | '上品' | '极品' | '绝世' | '传说';
export type 装备槽位 = '头部' | '胸部' | '盔甲' | '内衬' | '腿部' | '手部' | '足部' | '主武器' | '副武器' | '暗器' | '背部' | '腰部' | '坐骑';

// 身体部位定义
export type 身体部位 = '头部' | '胸部' | '腹部' | '左臂' | '右臂' | '左腿' | '右腿' | '手掌' | '足部';

// 随机词条/属性修饰
export interface 物品词条 {
    名称: string;      // e.g. "锋利"
    属性: string;      // e.g. "物理攻击"
    数值: number;     // e.g. 5
    类型: '固定值' | '百分比'; 
}

// 基础物品接口
export interface 基础物品 {
    ID: string;
    名称: string;
    描述: string;
    类型: 物品类型;
    品质: 物品品质;
    
    重量: number;        // 单位：斤（可小数，如 0.3 斤）
    
    // 堆叠系统
    堆叠数量: number;    // 当前堆叠数，默认为 1
    是否可堆叠: boolean; // 是否允许堆叠
    最大堆叠?: number;   // 单格最大堆叠数 (可选，默认99或999)

    价值: number;        // 铜钱
    
    当前耐久: number;
    最大耐久: number;    // 0为不可破坏

    词条列表: 物品词条[]; // 随机属性
    当前装备部位?: 装备槽位; // 当前穿戴/持有的装备槽位
}

// 武器扩展
export interface 武器 extends 基础物品 {
    类型: '武器';
    武器子类: '剑' | '刀' | '枪' | '棍' | '暗器' | '拳套';
    最小攻击: number;
    最大攻击: number;
    攻速修正: number; // 1.0 为标准
    格挡率: number;   // 0-100
}

// 防具扩展
export interface 防具 extends 基础物品 {
    类型: '防具';
    装备位置: '头部' | '胸部' | '腿部' | '手部' | '足部';
    覆盖部位: 身体部位[]; // 实际保护的部位
    
    物理防御: number;
    内功防御: number;
}

// 饰品扩展
export interface 饰品 extends 基础物品 {
    类型: '饰品';
}

// 秘籍扩展
export interface 秘籍 extends 基础物品 {
    类型: '秘籍';
    对应技能名: string;
    学习要求: string; // e.g. "悟性>50"
    研读进度: number;    // 0-100
    是否消耗: boolean; // 是否阅后即焚
}

// 消耗品扩展
export interface 消耗品 extends 基础物品 {
    类型: '消耗品';
    使用效果: {
        目标属性: string; // e.g. "当前气血", "当前内力"
        数值: number;
    }[];
    毒性: number; // 丹毒
}

// 联合类型
export type 游戏物品 = 武器 | 防具 | 饰品 | 秘籍 | 消耗品 | 基础物品;
