import type { 剧情风格类型, NTL后宫档位 } from '../../../models/system';
import type { 游戏设置结构 } from '../../../types';
import { 构建修炼体系附加块, 按功能开关过滤提示词内容 } from '../../../utils/promptFeatureToggles';
import { 剧情风格提示词_后宫 } from './harem';
import { 剧情风格提示词_修炼 } from './cultivation';
import { 剧情风格提示词_一般 } from './general';
import { 剧情风格提示词_修罗场 } from './shura';
import { 剧情风格提示词_纯爱 } from './pureLove';
import { 剧情风格提示词_NTL后宫_禁止乱伦, 剧情风格提示词_NTL后宫_假乱伦, 剧情风格提示词_NTL后宫_无限制 } from './ntlHarem';

const 风格提示词映射: Record<剧情风格类型, string> = {
    后宫: 剧情风格提示词_后宫,
    修炼: 构建修炼体系附加块(剧情风格提示词_修炼),
    一般: 剧情风格提示词_一般,
    修罗场: 剧情风格提示词_修罗场,
    纯爱: 剧情风格提示词_纯爱,
    NTL后宫: 剧情风格提示词_NTL后宫_无限制
};

export const 获取剧情风格提示词 = (style: 剧情风格类型, ntlTier?: NTL后宫档位): string => {
    if (style === 'NTL后宫') {
        if (ntlTier === '假乱伦') return 剧情风格提示词_NTL后宫_假乱伦;
        if (ntlTier === '无限制') return 剧情风格提示词_NTL后宫_无限制;
        if (ntlTier === '禁止乱伦') return 剧情风格提示词_NTL后宫_禁止乱伦;
        return 剧情风格提示词_NTL后宫_无限制;
    }
    return 风格提示词映射[style] || 风格提示词映射['一般'];
};

const 主剧情叙事总约束 = [
    '【主剧情叙事总约束】',
    '- 叙事必须去神话化：不要把人物、事件、机缘或关系写成宿命、天命、神授、不可违逆的传奇神话。',
    '- 叙事必须去魅主：不要让 NPC 或群体无理由地围绕主角倾倒、膜拜、臣服、迷恋或天然偏爱主角。',
    '- 叙事必须去发情化：人物互动以情境、利益、关系、礼法、风险和现实欲求推进，不要动辄写成性冲动主导。',
    '- 叙事必须去权谋化：不要把日常接触强行拔高成深谋远虑、局中局、政治博弈或大棋布局，除非上下文已有明确证据。',
    '- 叙事必须去利用化：不要默认人物彼此都在算计、利用、操控；若存在利用关系，必须有明确动机、条件与迹象支撑。',
    '- 叙事必须去阴暗化：不要持续渲染病态、压抑、恶意、扭曲、黑化、折磨等气质，除非当前情境已明确进入该状态。',
    '- 叙事必须去征服化：不要把互动写成收服、碾压、支配、征服；人物关系应按现实接触慢慢变化。',
    '- 叙事必须去绝望化：不要无端把局势写到不可挽回、全盘崩坏、彻底绝望；风险应与证据、资源、处境相匹配。',
    '- 一切推进都要符合现实逻辑：先有动因、限制、时间、成本、风险，再有行动与结果，让因果链完整可追。',
    '- 优先写可观察事实、可理解动机、可执行行为、可验证后果，让人物像现实中的人，而不是被设定推着走的符号。',
    '- 表达保持沉浸：天赋、资质、悟性、熟练度、直觉与感知力都优先写成身体经验、反应速度、观察结果与手上分寸，不写成面板说明式解释句。',
    '- 正常推进时，场面应该靠反馈、动作、回应与局面变化继续流动，不把“你想做什么”“你准备怎么办”当作回合末尾固定句式。',
    '- NPC应该保有自己的事情、位置、节奏与边界；就算愿意配合，也应给出具体回应，不写成待命物件。',
    '- 若场景已自然进入 NSFW 内容，应使用直白、清楚、可感的身体词汇推进描写，不把关键动作写成含糊代称。',
    '- 玩家输入是本回合主承接轴：主线规划、女主规划、群像排期、待触发事件都服务于当前动作的承接与局面变化，不直接越过玩家明确行动另起主轴。',
    '- 若现场出现足以改变承接方向的可验证外因，可自然写成受阻、改道或未竟；没有外因时，不为了别线排期或戏剧效果硬截玩家动作。'
].join('\n');

export const 构建剧情风格助手提示词 = (
    style: 剧情风格类型,
    ntlTier?: NTL后宫档位,
    config?: Partial<游戏设置结构> | null
): string => {
    return 按功能开关过滤提示词内容(
        `${主剧情叙事总约束}\n\n【剧情风格偏好】\n${获取剧情风格提示词(style, ntlTier)}`,
        config
    );
};
