const 构建主角建档自然语言摘要 = (
    charData: any,
    options?: { cultivationSystemEnabled?: boolean }
): string => {
    const 启用修炼体系 = options?.cultivationSystemEnabled !== false;
    const 纯文本 = (value: unknown, fallback = '未提供'): string => {
        if (typeof value !== 'string') return fallback;
        const trimmed = value.trim();
        return trimmed || fallback;
    };
    const 数值文本 = (value: unknown, fallback = '未提供'): string => (
        typeof value === 'number' && Number.isFinite(value) ? String(value) : fallback
    );
    const 天赋列表 = Array.isArray(charData?.天赋列表)
        ? charData.天赋列表
            .map((item: any) => 纯文本(item?.名称, ''))
            .filter(Boolean)
            .join('、')
        : '';
    const 背景名称 = 纯文本(charData?.出身背景?.名称, '未提供');
    const 背景描述 = 纯文本(charData?.出身背景?.描述, '未提供');
    return [
        '【主角建档信息】',
        `- 主角姓名：${纯文本(charData?.姓名)}`,
        `- 性别：${纯文本(charData?.性别)}`,
        `- 年龄：${数值文本(charData?.年龄)}`,
        `- 出生日期：${纯文本(charData?.出生日期)}`,
        `- 外貌：${纯文本(charData?.外貌)}`,
        `- 性格：${纯文本(charData?.性格)}`,
        ...(启用修炼体系 ? [`- 初始境界：${纯文本(charData?.境界)}`] : []),
        `- 六维：力量 ${数值文本(charData?.力量)} / 敏捷 ${数值文本(charData?.敏捷)} / 体质 ${数值文本(charData?.体质)} / 根骨 ${数值文本(charData?.根骨)} / 悟性 ${数值文本(charData?.悟性)} / 福源 ${数值文本(charData?.福源)}`,
        `- 天赋：${天赋列表 || '无'}`,
        `- 出身背景：${背景名称}`,
        `- 背景描述：${背景描述}`,
        '以上信息仅用于避免世界观与建档设定发生硬冲突，不代表这些内容已经作为前端变量或开局状态自动落地。'
    ].join('\n');
};

